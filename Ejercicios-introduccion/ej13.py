#Escribe un programa que solicite al usuario su nombre y su edad, y luego imprima un mensaje que indique cuántos años tendrá el usuario en 10 años más.
nombre = input("¿Cuál es tu nombre? ")
edad = int(input("¿Cuántos años tienes? "))

edad_en_10_anios = edad + 10;

print(nombre, " tendra ", edad_en_10_anios, " años, dentro de 10 años")